    private void moveObjetoParaTela(Aluno al) {
        txtMatricula.setText(Integer.toString(al.getCodAluno()));
        txtNome.setText(al.getNome());
        txtTurma.setText(al.getTurma());
    }
    
    private Aluno moveTelaParaObjeto() {
        aluno = new Aluno();
        aluno.setCodAluno(Integer.parseInt(txtMatricula.getText()));
        aluno.setNome(txtNome.getText());
        aluno.setTurma(txtTurma.getText());
        return aluno;
    }
    
    private void limparCampos() {
        txtMatricula.setText("");
        txtNome.setText("");
        txtTurma.setText("");
        txtMatricula.requestFocus();
    }
    
    private void mensagem(String msg) {
        JOptionPane.showMessageDialog(rootPane, msg, "Aviso ao Usuário", JOptionPane.INFORMATION_MESSAGE);
    }


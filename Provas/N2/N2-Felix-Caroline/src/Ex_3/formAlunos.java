    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        if (txtMatricula.getText().equals("") || txtNome.getText().equals("") || txtTurma.getText().equals("")) {
            mensagem("Um ou mais campos não preenchidos!");
        } else {
            aluno = moveTelaParaObjeto();
            try {
                if (alunoDAO.insere(aluno)) {
                    mensagem("Aluno inserido com sucesso!");
                    limparCampos();
                } else {
                    mensagem("Não foi possível incluir o Aluno!");
                }
            } catch (SQLException ex) {
                mensagem("Erro de Banco" + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnIncluirActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        aluno = moveTelaParaObjeto();
        try {
            aluno = alunoDAO.buscarID(aluno);
            if (aluno != null) {
                moveObjetoParaTela(aluno);
            } else {
                mensagem("Aluno não encontrado!");
                limparCampos();
            }
        } catch (SQLException ex) {
            mensagem("Erro de Banco" + ex.getMessage());
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

